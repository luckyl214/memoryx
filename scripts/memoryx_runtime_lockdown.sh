clone_or_update_runtime_checkout() {
  need_cmd git
  assert_stable_tag_remote

  mkdir -p "$MEMORYX_RUNTIME_ROOT"

  if [[ -d "$MEMORYX_RUNTIME_DIR/.git" ]]; then
    log "Existing runtime checkout found: $MEMORYX_RUNTIME_DIR"
    cd "$MEMORYX_RUNTIME_DIR"

    if [[ -n "$(git status --short)" ]]; then
      die "Runtime checkout is dirty. Refusing to proceed: $MEMORYX_RUNTIME_DIR"
    fi

    git fetch --tags origin
    git checkout "$MEMORYX_STABLE_TAG"
  else
    log "Cloning MemoryX stable runtime to: $MEMORYX_RUNTIME_DIR"
    git clone "$MEMORYX_REPO_URL" "$MEMORYX_RUNTIME_DIR"
    cd "$MEMORYX_RUNTIME_DIR"
    git fetch --tags origin
    git checkout "$MEMORYX_STABLE_TAG"
  fi

  local tag_type
  tag_type="$(git cat-file -t "$MEMORYX_STABLE_TAG" || true)"
  [[ "$tag_type" == "tag" ]] || die "$MEMORYX_STABLE_TAG is not an annotated tag. got: $tag_type"

  local commit
  commit="$(git rev-list -n 1 "$MEMORYX_STABLE_TAG" --abbrev-commit)"
  log "Runtime checkout pinned: $MEMORYX_STABLE_TAG -> $commit"
}

create_runtime_venv_and_install() {
  need_cmd "$PYTHON_BIN"

  cd "$MEMORYX_RUNTIME_DIR"

  log "Creating runtime venv with $PYTHON_BIN"
  "$PYTHON_BIN" -m venv .venv

  # shellcheck disable=SC1091
  . .venv/bin/activate

  python -m pip install --upgrade pip -q

  log "Installing MemoryX stable package"
  python -m pip install -q .

  log "Installing dev extras for smoke/test utilities if available"
  python -m pip install -q ".[dev]" || {
    log "WARNING: .[dev] install failed or unavailable. Core package install already succeeded."
  }

  log "Installed package version:"
  python - <<PY
import memoryx
print("memoryx.__version__ =", getattr(memoryx, "__version__", "UNKNOWN"))
assert getattr(memoryx, "__version__", None) == "$MEMORYX_EXPECTED_VERSION"
PY
}

