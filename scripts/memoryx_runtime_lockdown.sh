write_hermes_preflight() {
  local py="$MEMORYX_HOME/bin/hermes_memoryx_preflight.py"
  local sh="$MEMORYX_HOME/bin/hermes_memoryx_preflight.sh"

  log "Writing Hermes MemoryX preflight: $py"

  cat > "$py" <<'PYEOF'
#!/usr/bin/env python3
"""
Hermes -> MemoryX preflight.

Purpose:
- fail fast if Hermes is not using the stable MemoryX runtime
- prevent accidental use of dev checkout or wrong version
- verify runtime DB path is outside the git repository
"""

from __future__ import annotations

import os
import sys
from pathlib import Path


EXPECTED = os.environ.get("MEMORYX_EXPECTED_VERSION", "2.0.0")


def fail(msg: str) -> None:
    print(f"[hermes_memoryx_preflight] FAIL: {msg}", file=sys.stderr)
    raise SystemExit(1)


def ok(msg: str) -> None:
    print(f"[hermes_memoryx_preflight] PASS: {msg}")


def main() -> None:
    try:
        import memoryx
    except Exception as exc:
        fail(f"cannot import memoryx: {exc!r}")

    actual = getattr(memoryx, "__version__", None)
    if actual != EXPECTED:
        fail(f"memoryx version mismatch: expected={EXPECTED!r}, actual={actual!r}")

    module_file = Path(getattr(memoryx, "__file__", "")).resolve()
    ok(f"memoryx version {actual}")
    ok(f"memoryx module {module_file}")

    db_path = Path(os.environ.get("MEMORYX_DB_PATH", "")).expanduser()
    if not db_path:
        fail("MEMORYX_DB_PATH is not set")

    db_path = db_path.resolve()
    home = Path(os.environ.get("MEMORYX_HOME", str(Path.home() / ".memoryx"))).expanduser().resolve()

    if home not in [db_path, *db_path.parents]:
        fail(f"MEMORYX_DB_PATH must be under MEMORYX_HOME. db={db_path}, home={home}")

    if ".git" in db_path.parts:
        fail(f"MEMORYX_DB_PATH must not be inside a git repo: {db_path}")

    if db_path.name in {"memoryx.db", "memoryx.sqlite", "memoryx_public_smoke.db"}:
        ok(f"runtime db path {db_path}")
    else:
        ok(f"runtime db path {db_path}")

    # Repository import smoke.
    try:
        from memoryx.storage.repository import MemoryRepository
    except Exception as exc:
        fail(f"cannot import MemoryRepository: {exc!r}")

    ok("MemoryRepository import")

    print("[hermes_memoryx_preflight] ALL PASS")


if __name__ == "__main__":
    main()
PYEOF

  chmod +x "$py"

  cat > "$sh" <<SHEOF
#!/usr/bin/env bash
set -Eeuo pipefail

source "$MEMORYX_ENV_FILE"
source "$MEMORYX_RUNTIME_DIR/.venv/bin/activate"
python "$py"
SHEOF

  chmod +x "$sh"

  log "Hermes preflight shell wrapper: $sh"
}

