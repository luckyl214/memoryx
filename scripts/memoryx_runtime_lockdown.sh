  write_memoryx_env
  write_hermes_preflight

  cat <<EOF

Hermes config ready.

To load before Hermes starts:
source "$HERMES_MEMORYX_ENV_FILE"
"$MEMORYX_HOME/bin/hermes_memoryx_preflight.sh"

Recommended Hermes startup guard:
call "$MEMORYX_HOME/bin/hermes_memoryx_preflight.sh" before enabling any MemoryX plugin hooks.
EOF
}

run_smoke() {
  source "$MEMORYX_ENV_FILE"
  "$MEMORYX_HOME/bin/memoryx_runtime_smoke.sh"
  "$MEMORYX_HOME/bin/hermes_memoryx_preflight.sh"
}

show_status() {
  cat <<EOF
MemoryX stable runtime status

MEMORYX_REPO_URL:          $MEMORYX_REPO_URL
MEMORYX_STABLE_TAG:        $MEMORYX_STABLE_TAG
MEMORYX_EXPECTED_VERSION:  $MEMORYX_EXPECTED_VERSION
MEMORYX_RUNTIME_DIR:       $MEMORYX_RUNTIME_DIR
MEMORYX_HOME:              $MEMORYX_HOME
MEMORYX_ENV_FILE:          $MEMORYX_ENV_FILE
HERMES_MEMORYX_ENV_FILE:   $HERMES_MEMORYX_ENV_FILE

Commands:
source "$MEMORYX_ENV_FILE"
"$MEMORYX_HOME/bin/memoryx_runtime_smoke.sh"
"$MEMORYX_HOME/bin/hermes_memoryx_preflight.sh"
EOF
}

usage() {
  cat <<EOF
Usage:
$0 all
$0 setup-runtime
$0 setup-hermes-config
$0 install-repo-guardrails
$0 smoke
$0 status

Recommended:
$0 all
EOF
}

main() {
  local cmd="${1:-}"
  local initial_dir
  initial_dir="$(repo_root_or_die)"

  case "$cmd" in
    all)
      setup_runtime
      cd "$initial_dir"
      install_repo_guardrails
      run_smoke
      show_status
      ;;
    setup-runtime)
      setup_runtime
      ;;
    setup-hermes-config)
      setup_hermes_config
      ;;
    install-repo-guardrails)
      install_repo_guardrails
      ;;
    smoke)
      run_smoke
      ;;
    status)
      show_status
      ;;
    *)
      usage
      exit 1
      ;;
  esac
}

main "$@"
