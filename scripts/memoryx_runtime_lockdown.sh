#!/usr/bin/env bash
# scripts/memoryx_runtime_lockdown.sh
#
# MemoryX 2.0.0 stable runtime + guardrails setup.
#
# Safe by design:
# - does NOT move or create release tags
# - does NOT publish GitHub releases
# - does NOT edit runtime DB contents
# - does NOT modify MemoryX core logic
# - only creates operational guard/config files and a stable runtime checkout
#
# Usage:
#   chmod +x scripts/memoryx_runtime_lockdown.sh
#   ./scripts/memoryx_runtime_lockdown.sh all
#
# Optional:
#   MEMORYX_REPO_URL=https://github.com/luckyl214/memoryx.git ./scripts/memoryx_runtime_lockdown.sh all
#   MEMORYX_RUNTIME_ROOT="$HOME/runtime" ./scripts/memoryx_runtime_lockdown.sh setup-runtime
#   MEMORYX_EXPECTED_VERSION=2.0.0 ./scripts/memoryx_runtime_lockdown.sh smoke

set -Eeuo pipefail

MEMORYX_REPO_URL="${MEMORYX_REPO_URL:-https://github.com/luckyl214/memoryx.git}"
MEMORYX_STABLE_TAG="${MEMORYX_STABLE_TAG:-v2.0.0}"
MEMORYX_EXPECTED_VERSION="${MEMORYX_EXPECTED_VERSION:-2.0.0}"

MEMORYX_RUNTIME_ROOT="${MEMORYX_RUNTIME_ROOT:-$HOME/runtime}"
MEMORYX_RUNTIME_DIR="${MEMORYX_RUNTIME_DIR:-$MEMORYX_RUNTIME_ROOT/memoryx-$MEMORYX_EXPECTED_VERSION}"
MEMORYX_HOME="${MEMORYX_HOME:-$HOME/.memoryx}"
MEMORYX_ENV_FILE="${MEMORYX_ENV_FILE:-$MEMORYX_HOME/memoryx.env}"
HERMES_CONFIG_DIR="${HERMES_CONFIG_DIR:-$HOME/.config/hermes}"
HERMES_MEMORYX_ENV_FILE="${HERMES_MEMORYX_ENV_FILE:-$HERMES_CONFIG_DIR/memoryx.env}"

PYTHON_BIN="${PYTHON_BIN:-python3.12}"

SCRIPT_NAME="$(basename "$0")"

log() {
  printf '\n[%s] %s\n' "$SCRIPT_NAME" "$*"
}

die() {
  printf '\n[%s] ERROR: %s\n' "$SCRIPT_NAME" "$*" >&2
  exit 1
}

need_cmd() {
  command -v "$1" >/dev/null 2>&1 || die "Required command not found: $1"
}

repo_root_or_die() {
  git rev-parse --show-toplevel >/dev/null 2>&1 || die "Run this script inside the MemoryX git repository."
  git rev-parse --show-toplevel
}

assert_not_dirty_for_repo_guardrails() {
  local root
  root="$(repo_root_or_die)"
  cd "$root"

  if [[ -n "$(git status --short)" ]]; then
    cat >&2 <<EOF

[$SCRIPT_NAME] Worktree is not clean.

This script refuses to install repository guardrails into a dirty tree because
we do not want to mix guardrail files with unrelated edits.

Run:
  git status --short

Then either commit/stash your current changes or run:
  ./scripts/memoryx_runtime_lockdown.sh setup-runtime
  ./scripts/memoryx_runtime_lockdown.sh setup-hermes-config

EOF
    exit 1
  fi
}

assert_stable_tag_remote() {
  need_cmd git

  log "Checking remote stable tag exists: $MEMORYX_STABLE_TAG"
  git ls-remote --tags "$MEMORYX_REPO_URL" "$MEMORYX_STABLE_TAG" | grep -q "$MEMORYX_STABLE_TAG" \
    || die "Remote tag not found: $MEMORYX_STABLE_TAG at $MEMORYX_REPO_URL"
}

setup_runtime_dirs() {
  log "Creating runtime directories under $MEMORYX_HOME"

  mkdir -p "$MEMORYX_HOME/data"
  mkdir -p "$MEMORYX_HOME/traces"
  mkdir -p "$MEMORYX_HOME/logs"
  mkdir -p "$MEMORYX_HOME/vector"
  mkdir -p "$MEMORYX_HOME/backups"
  mkdir -p "$MEMORYX_HOME/bin"
  mkdir -p "$HERMES_CONFIG_DIR"

  chmod 700 "$MEMORYX_HOME"
  chmod 700 "$MEMORYX_HOME/data" "$MEMORYX_HOME/traces" "$MEMORYX_HOME/logs" "$MEMORYX_HOME/vector" "$MEMORYX_HOME/backups"
}

